package Tasks;
//3.check if a character is a vowel or consonant using switch case
public class Task_12 {

	public static void main(String[] args) {
        char c='+';
        switch(c) {
        case 'a':
        	System.out.println(c+" is vowel");
        	break;
        case 'e':
        	System.out.println(c+" is vowel");
        	break;
        case 'i':
        	System.out.println(c+" is vowel");
        	break;
        case 'o':
        	System.out.println(c+" is vowel");
        	break;
        case 'u':
        	System.out.println(c+" is vowel");
        	break;
        default:
        	System.out.println(c+" is consonant");
        }
        
	}

}
